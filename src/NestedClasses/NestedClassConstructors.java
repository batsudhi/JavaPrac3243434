package NestedClasses;

public class NestedClassConstructors {

	static class NestedClass
	{
		NestedClass(){
			//first constructor
		}
		
		NestedClass(int i){
			//first constructor
		}
		

		NestedClass(int i, int j){
			//first constructor
		}
		
		void methodOne()
        {
            //Overloaded method
        }
 
        void methodOne(int i)
        {
            //Overloaded method
        }
 
        void methodOne(int i, int j)
        {
            //Overloaded method
        }
	}
}
